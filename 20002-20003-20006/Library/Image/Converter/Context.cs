using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ImageProcess.Converter
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A context. </summary>
    ///
    /// <remarks>   Viki, 8/1/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    class Context
    {
       /// <summary>    The convert. </summary>
       protected BaseConvert convert;

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Sets a strategy. </summary>
        ///
        /// <remarks>   Viki, 8/1/2018. </remarks>
        ///
        /// <param name="convert">  The convert. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public void SetStrategy(BaseConvert convert)
        {
            this.convert = convert;
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Executes this object. </summary>
        ///
        /// <remarks>   Viki, 8/1/2018. </remarks>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public void Execute()
        {
            this.convert.Execute();
        }
    }
}
