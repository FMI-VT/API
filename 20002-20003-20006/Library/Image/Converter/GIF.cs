using System;
using System.Collections.Generic;
using System.Drawing;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ImageProcess.Converter
{
    ////////////////////////////////////////////////////////////////////////////////////////////////////
    /// <summary>   A gif. </summary>
    ///
    /// <remarks>   Viki, 8/1/2018. </remarks>
    ////////////////////////////////////////////////////////////////////////////////////////////////////

    class GIF : BaseConvert
    {
        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Constructor. </summary>
        ///
        /// <remarks>   Viki, 8/1/2018. </remarks>
        ///
        /// <param name="srcPath">  Full pathname of the source file. </param>
        /// <param name="destPath"> Full pathname of the destination file. </param>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public GIF(string srcPath, string destPath) : base(srcPath, destPath)
        {
        }

        ////////////////////////////////////////////////////////////////////////////////////////////////////
        /// <summary>   Executes this object. </summary>
        ///
        /// <remarks>   Viki, 8/1/2018. </remarks>
        ////////////////////////////////////////////////////////////////////////////////////////////////////

        public override void Execute()
        {
            Image image = Image.FromFile(this.srcPath);
            image.Save(this.destPath, System.Drawing.Imaging.ImageFormat.Gif);
        }
    }
}

